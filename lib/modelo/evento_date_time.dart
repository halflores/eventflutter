  final String? diaIni;
  final String? mesIni;
  final String? anioIni;
  final String? horaIni;
  final String? minutoIni;
  final DateTime? fechaFinal;
  final String? diaFin;
  final String? mesFin;
  final String? anioFin;
  final String? horaFin;
